class Wall {
    constructor(sx, sy, ex, ey) {
        var options = {
            restitution: 1,
            friction: 0,
            isStatic: true
        };
        this.w = wallWidth;
        this.h = Math.sqrt((sx - ex) * (sx - ex) + (sy - ey) * (sy - ey));
        this.body = Bodies.rectangle(sx, sy, this.w, this.h, options);

        if (sx > ex) {
            Body.rotate(this.body, Math.asin((sx - ex) / this.h));
            Body.translate(this.body, { x: -1 * wallMultipleIndex[curRows] * (plinkoDistanceX[curRows] / 2), y: wallMultipleIndex[curRows] * plinkoDistanceY[curRows] });
        } else {
            Body.rotate(this.body, -1 * Math.asin((ex - sx) / this.h));
            Body.translate(this.body, { x: wallMultipleIndex[curRows] * (plinkoDistanceX[curRows] / 2), y: wallMultipleIndex[curRows] * plinkoDistanceY[curRows] });
        }

        World.add(world, this.body);
    }

    destroy() {
        World.remove(world, this.body);
    }
};

