class Particle {
    constructor(x, y, r, risk, index, balance) {
        var options = {
            restitution: 0.6,
            collisionFilter: {
                'group': -1
            },

        };
        this.r = r;
        this.body = Bodies.circle(x, y, this.r, options);
        this.risk = risk;
        this.index = index;
        this.balance = balance;

        World.add(world, this.body);
    }

    display(p5) {
        var pos = this.body.position;
        var angle = this.body.angle;

        p5.push();
        p5.translate(pos.x, pos.y);
        p5.rotate(angle);
        p5.noStroke();
        p5.fill(particleColor[this.risk].r1, particleColor[this.risk].g1, particleColor[this.risk].b1);
        p5.ellipseMode(p5.RADIUS);
        p5.ellipse(0, 0, this.r, this.r);
        p5.fill(particleColor[this.risk].r2, particleColor[this.risk].g2, particleColor[this.risk].b2);
        p5.ellipseMode(p5.RADIUS);
        p5.ellipse(0, 0, this.r / 3 * 2, this.r / 3 * 2);
        p5.fill(particleColor[this.risk].r1, particleColor[this.risk].g1, particleColor[this.risk].b1);
        p5.ellipseMode(p5.RADIUS);
        p5.ellipse(0, 0, (this.r / 3 * 2) - 1.5, (this.r / 3 * 2) - 1.5);
        p5.pop();
    }

    destroy() {
        World.remove(world, this.body);
    }
};

class Ripple {
    constructor(x, y, r, o, id) {
        var options = {
            restitution: 1,
            friction: 0,
            isStatic: true
        };
        this.r = r;
        this.origin = o;
        this.body = Bodies.circle(x, y, this.r, options);
        this.id = id;
    }

    display(p5) {
        var pos = this.body.position;
        var angle = this.body.angle;

        p5.push();
        p5.translate(pos.x, pos.y);
        p5.rotate(angle);
        p5.imageMode(p5.CENTER);
        p5.noStroke();
        p5.fill(255 - (this.r - this.origin) * 13, 255 - (this.r - this.origin) * 13, 255 - (this.r - this.origin) * 13);
        p5.ellipseMode(p5.RADIUS);
        p5.ellipse(0, 0, this.r, this.r);
        p5.pop();

        this.r += 0.4;
    }
};

